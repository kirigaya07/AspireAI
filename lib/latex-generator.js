/**
 * Real LaTeX (.tex) resume generator.
 * Converts resume markdown to compilable LaTeX — Overleaf-compatible.
 * Supports 3 templates: CLASSIC (Jake's Resume), MODERN, MINIMAL
 */

// ── Escape helpers ────────────────────────────────────────────────

function escapeLatex(str) {
  if (!str) return "";
  return String(str)
    .replace(/\\/g, "\\textbackslash{}")
    .replace(/&/g, "\\&")
    .replace(/%/g, "\\%")
    .replace(/\$/g, "\\$")
    .replace(/#/g, "\\#")
    .replace(/_/g, "\\_")
    .replace(/\{/g, "\\{")
    .replace(/\}/g, "\\}")
    .replace(/~/g, "\\textasciitilde{}")
    .replace(/\^/g, "\\textasciicircum{}");
}

// Inline markdown (**bold**, *italic*, `code`) → LaTeX, with special char escaping
function inlineLatex(text) {
  if (!text) return "";
  const parts = [];
  const regex = /\*\*([^*]+)\*\*|\*([^*]+)\*|`([^`]+)`|([^*`]+)/g;
  let m;
  while ((m = regex.exec(text)) !== null) {
    if (m[1] !== undefined) parts.push(`\\textbf{${escapeLatex(m[1])}}`);
    else if (m[2] !== undefined) parts.push(`\\textit{${escapeLatex(m[2])}}`);
    else if (m[3] !== undefined) parts.push(`\\texttt{${escapeLatex(m[3])}}`);
    else if (m[4] !== undefined) parts.push(escapeLatex(m[4]));
  }
  return parts.join("");
}

// Format a contact item — linkify URLs and emails
function formatContact(item) {
  const t = item.trim();
  if (/^https?:\/\//.test(t)) return `\\href{${t}}{${escapeLatex(t)}}`;
  if (t.includes("@")) return `\\href{mailto:${t}}{${escapeLatex(t)}}`;
  return escapeLatex(t);
}

// ── Markdown parser ───────────────────────────────────────────────

/**
 * Parses resume markdown into structured data.
 * @returns {{ name: string, contact: string[], sections: Array }}
 */
function parseMarkdown(markdown) {
  const lines = markdown.split("\n");
  const data = { name: "", contact: [], sections: [] };
  let section = null;
  let entry = null;
  let i = 0;

  function pushEntry() {
    if (entry && section) {
      section.entries.push(entry);
      entry = null;
    }
  }

  while (i < lines.length) {
    const t = lines[i].trim();
    i++;

    if (!t) continue;

    // # Name
    if (t.startsWith("# ")) {
      data.name = t.slice(2).trim();
      continue;
    }

    // Contact line (pipe-separated)
    if (t.includes("|") && !t.startsWith("#") && !t.startsWith("-") && !t.startsWith("*")) {
      data.contact = t.split("|").map((p) => p.trim()).filter(Boolean);
      continue;
    }

    // ## Section
    if (t.startsWith("## ")) {
      pushEntry();
      section = { title: t.slice(3).trim(), text: "", entries: [] };
      data.sections.push(section);
      continue;
    }

    // ### Entry
    if (t.startsWith("### ")) {
      pushEntry();
      entry = { title: t.slice(4).trim(), date: "", subtitle: "", bullets: [] };
      // Peek ahead: date line (bold or year), then subtitle
      while (i < lines.length) {
        const peek = lines[i].trim();
        if (!peek) break;
        if (peek.startsWith("**") || /^\d{4}/.test(peek) || /^present$/i.test(peek)) {
          entry.date = peek.replace(/\*\*/g, "").trim();
          i++;
        } else if (peek.startsWith("*") && peek.endsWith("*") && !peek.startsWith("**")) {
          entry.subtitle = peek.slice(1, -1).trim();
          i++;
        } else {
          break;
        }
      }
      continue;
    }

    // Bullet
    if (t.startsWith("- ") || t.startsWith("* ")) {
      if (entry) entry.bullets.push(t.slice(2).trim());
      continue;
    }

    // Plain text line
    if (section && !entry) {
      section.text += (section.text ? " " : "") + t;
    } else if (entry && !entry.subtitle) {
      entry.subtitle = t;
    }
  }

  pushEntry();
  return data;
}

// ── Template: CLASSIC (Jake's Resume) ────────────────────────────

function generateClassic(data) {
  const { name, contact, sections } = data;

  const preamble = `%% Generated by AspireAI — Classic (Jake's Resume style)
%% Compile with pdfLaTeX on Overleaf.
\\documentclass[letterpaper,11pt]{article}

\\usepackage{latexsym}
\\usepackage[empty]{fullpage}
\\usepackage{titlesec}
\\usepackage{marvosym}
\\usepackage[usenames,dvipsnames]{color}
\\usepackage{verbatim}
\\usepackage{enumitem}
\\usepackage[hidelinks]{hyperref}
\\usepackage{fancyhdr}
\\usepackage[english]{babel}
\\usepackage{tabularx}
\\input{glyphtounicode}

\\pagestyle{fancy}
\\fancyhf{}
\\fancyfoot{}
\\renewcommand{\\headrulewidth}{0pt}
\\renewcommand{\\footrulewidth}{0pt}

\\addtolength{\\oddsidemargin}{-0.5in}
\\addtolength{\\evensidemargin}{-0.5in}
\\addtolength{\\textwidth}{1in}
\\addtolength{\\topmargin}{-.5in}
\\addtolength{\\textheight}{1.0in}

\\urlstyle{same}
\\raggedbottom
\\raggedright
\\setlength{\\tabcolsep}{0in}

\\titleformat{\\section}{\\vspace{-4pt}\\scshape\\raggedright\\large}{}{0em}{}[\\color{black}\\titlerule\\vspace{-5pt}]

\\pdfgentounicode=1

% ── Custom commands ──────────────────────────────────────────────
\\newcommand{\\resumeItem}[1]{\\item\\small{#1\\vspace{-2pt}}}

\\newcommand{\\resumeSubheading}[4]{
  \\vspace{-2pt}\\item
  \\begin{tabular*}{0.97\\textwidth}[t]{l@{\\extracolsep{\\fill}}r}
    \\textbf{#1} & #2 \\\\
    \\textit{\\small#3} & \\textit{\\small#4} \\\\
  \\end{tabular*}\\vspace{-7pt}
}

\\newcommand{\\resumeProjectHeading}[2]{
  \\item
  \\begin{tabular*}{0.97\\textwidth}{l@{\\extracolsep{\\fill}}r}
    \\small#1 & #2 \\\\
  \\end{tabular*}\\vspace{-7pt}
}

\\newcommand{\\resumeSubHeadingListStart}{\\begin{itemize}[leftmargin=0.15in,label={}]}
\\newcommand{\\resumeSubHeadingListEnd}{\\end{itemize}}
\\newcommand{\\resumeItemListStart}{\\begin{itemize}}
\\newcommand{\\resumeItemListEnd}{\\end{itemize}\\vspace{-5pt}}

%-------------------------------------------
\\begin{document}`;

  let body = "\n\n% ── Header ──────────────────────────────────────\n";
  body += `\\begin{center}\n`;
  body += `  {\\Huge\\scshape ${escapeLatex(name)}} \\\\[3pt]\n`;
  if (contact.length) {
    body += `  \\small ${contact.map(formatContact).join(" $|$ ")}\n`;
  }
  body += `\\end{center}\n\n`;

  for (const sec of sections) {
    body += `% ── ${sec.title} ──\n`;
    body += `\\section{${escapeLatex(sec.title)}}\n`;

    // Text-only section (Summary, Skills)
    if (!sec.entries.length && sec.text) {
      body += `\\small{${inlineLatex(sec.text)}}\n\n`;
      continue;
    }

    if (sec.entries.length) {
      body += `\\resumeSubHeadingListStart\n`;
      for (const e of sec.entries) {
        body += `  \\resumeSubheading\n`;
        body += `    {${inlineLatex(e.title)}}{${inlineLatex(e.date)}}\n`;
        body += `    {${inlineLatex(e.subtitle)}}{}\n`;
        if (e.bullets.length) {
          body += `  \\resumeItemListStart\n`;
          for (const b of e.bullets) {
            body += `    \\resumeItem{${inlineLatex(b)}}\n`;
          }
          body += `  \\resumeItemListEnd\n`;
        }
      }
      body += `\\resumeSubHeadingListEnd\n\n`;
    }
  }

  body += `\\end{document}\n`;
  return preamble + body;
}

// ── Template: MODERN ─────────────────────────────────────────────

function generateModern(data) {
  const { name, contact, sections } = data;

  const preamble = `%% Generated by AspireAI — Modern Template
%% Compile with pdfLaTeX on Overleaf.
\\documentclass[11pt,a4paper]{article}

\\usepackage[T1]{fontenc}
\\usepackage[utf8]{inputenc}
\\usepackage[margin=0.75in,top=0.6in,bottom=0.6in]{geometry}
\\usepackage[hidelinks]{hyperref}
\\usepackage{enumitem}
\\usepackage{xcolor}
\\usepackage{titlesec}
\\usepackage{tabularx}
\\usepackage{parskip}

\\definecolor{accent}{RGB}{67,56,202}

\\pagestyle{empty}
\\setlength{\\parindent}{0pt}
\\setlength{\\parskip}{4pt}

\\titleformat{\\section}{\\color{accent}\\large\\bfseries}{}{0em}{}[{\\color{accent}\\titlerule[0.8pt]}]
\\titlespacing{\\section}{0pt}{10pt}{5pt}

\\newcommand{\\entry}[4]{%
  \\begin{tabularx}{\\linewidth}{Xr}
    \\textbf{#1} & \\small#2 \\\\
    \\textit{\\small\\color{gray}#3} & \\textit{\\small\\color{gray}#4}
  \\end{tabularx}\\vspace{-4pt}
}

\\begin{document}`;

  let body = "\n\n% ── Header ──────────────────────────────────────\n";
  body += `{\\LARGE\\bfseries\\color{accent} ${escapeLatex(name)}}\n\n`;
  if (contact.length) {
    body += `{\\small\\color{gray} ${contact.map(formatContact).join(" \\enspace\\textbar\\enspace ")}}\n\n`;
  }
  body += `\\vspace{4pt}\\noindent\\rule{\\linewidth}{0.8pt}\\vspace{4pt}\n\n`;

  for (const sec of sections) {
    body += `% ── ${sec.title} ──\n`;
    body += `\\section*{${escapeLatex(sec.title)}}\n`;

    if (!sec.entries.length && sec.text) {
      body += `${inlineLatex(sec.text)}\n\n`;
      continue;
    }

    if (sec.entries.length) {
      for (const e of sec.entries) {
        body += `\\entry{${inlineLatex(e.title)}}{${inlineLatex(e.date)}}{${inlineLatex(e.subtitle)}}{}\n`;
        if (e.bullets.length) {
          body += `\\begin{itemize}[leftmargin=1.2em,itemsep=0pt,topsep=2pt]\n`;
          for (const b of e.bullets) {
            body += `  \\item\\small ${inlineLatex(b)}\n`;
          }
          body += `\\end{itemize}\n`;
        }
        body += `\\vspace{4pt}\n`;
      }
      body += "\n";
    }
  }

  body += `\\end{document}\n`;
  return preamble + body;
}

// ── Template: MINIMAL ────────────────────────────────────────────

function generateMinimal(data) {
  const { name, contact, sections } = data;

  const preamble = `%% Generated by AspireAI — Minimal Template
%% Compile with pdfLaTeX on Overleaf.
\\documentclass[11pt,a4paper]{article}

\\usepackage[T1]{fontenc}
\\usepackage[utf8]{inputenc}
\\usepackage[margin=1in,top=0.8in]{geometry}
\\usepackage[hidelinks]{hyperref}
\\usepackage{enumitem}
\\usepackage{titlesec}
\\usepackage{parskip}
\\usepackage{tabularx}

\\pagestyle{empty}
\\setlength{\\parindent}{0pt}

\\titleformat{\\section}{\\small\\bfseries\\MakeUppercase}{}{0em}{}[\\titlerule]
\\titlespacing{\\section}{0pt}{12pt}{5pt}

\\newcommand{\\entry}[4]{%
  \\begin{tabularx}{\\linewidth}{Xr}
    \\textbf{#1} & \\small#2 \\\\
    \\textit{\\small#3} & \\textit{\\small#4}
  \\end{tabularx}\\vspace{-2pt}
}

\\begin{document}`;

  let body = "\n\n% ── Header ──────────────────────────────────────\n";
  body += `{\\Large\\bfseries ${escapeLatex(name)}}\n\n`;
  if (contact.length) {
    body += `{\\small ${contact.map(formatContact).join(" $\\cdot$ ")}}\n\n`;
  }
  body += `\\vspace{8pt}\n\n`;

  for (const sec of sections) {
    body += `% ── ${sec.title} ──\n`;
    body += `\\section*{${escapeLatex(sec.title)}}\n`;

    if (!sec.entries.length && sec.text) {
      body += `{\\small ${inlineLatex(sec.text)}}\n\n`;
      continue;
    }

    if (sec.entries.length) {
      for (const e of sec.entries) {
        body += `\\entry{${inlineLatex(e.title)}}{${inlineLatex(e.date)}}{${inlineLatex(e.subtitle)}}{}\n`;
        if (e.bullets.length) {
          body += `\\begin{itemize}[leftmargin=1em,itemsep=0pt,topsep=3pt,parsep=0pt]\n`;
          for (const b of e.bullets) {
            body += `  \\item[---] \\small ${inlineLatex(b)}\n`;
          }
          body += `\\end{itemize}\n`;
        }
        body += `\\vspace{4pt}\n`;
      }
      body += "\n";
    }
  }

  body += `\\end{document}\n`;
  return preamble + body;
}

// ── Public API ────────────────────────────────────────────────────

/**
 * Generates a real LaTeX (.tex) source file from resume markdown.
 * @param {string} markdownContent
 * @param {string} template — "CLASSIC" | "MODERN" | "MINIMAL"
 * @returns {string} LaTeX source
 */
export function generateLatex(markdownContent, template = "CLASSIC") {
  const data = parseMarkdown(markdownContent);
  switch (template) {
    case "MODERN":  return generateModern(data);
    case "MINIMAL": return generateMinimal(data);
    default:        return generateClassic(data);
  }
}
